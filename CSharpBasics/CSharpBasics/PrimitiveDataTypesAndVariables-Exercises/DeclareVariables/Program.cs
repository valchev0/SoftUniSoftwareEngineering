using System;

class DeclareVariables
{
    static void Main()
    {
        sbyte first = -115;
        byte second = 97;
        short third = -10000;
        ushort forth = 52130;
        int fifth = 4825932;
        Console.WriteLine("{0}\n {1}\n {2}\n {3}\n {4}\n", first, second, third, forth, fifth);
    }
}